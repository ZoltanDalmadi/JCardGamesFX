package hu.unideb.inf.JCardGamesFX.model;

/**
 * Standard French playing card suits.
 */
public enum FrenchSuit implements Suit {
  /**
   * Black clover.
   */
  Clubs,
  /**
   * Black "spearhead".
   */
  Spades,
  /**
   * Red heart.
   */
  Hearts,
  /**
   * Red diamond shape.
   */
  Diamonds
}
